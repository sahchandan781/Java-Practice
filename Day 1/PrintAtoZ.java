public class PrintAtoZ {
    public static void main(String[] args) {
        //for A to Z
        for(char i = 'A'; i<='Z'; i++){
            System.out.print(i+" ");
         }

         // for a to z
         for(char i = 'a'; i<='z'; i++){
            System.out.print(i+" ");
         }

         //for A to small z
         for(char i = 'A'; i<='z'; i++){
            System.out.print(i+" ");
         }
    }
}
