package Day9String;


public class PretttyPrinting {
    public static void main(String[] args) {
        float a = 453.1234f;
        System.out.printf("Formatted number is %.2f", a); // it is rounding off
        System.out.println();

        System.out.printf("Pie: %.3f", Math.PI);

        System.out.println();

        System.out.printf("Hello my name is %s and I am %s", "Chandan", "cool");

    }

    
}